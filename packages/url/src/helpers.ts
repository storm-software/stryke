/**
 * A regex source string that matches any of the valid TLDs (case-insensitive).
 */
const VALID_TLD_PATTERN = `(?:${[...new Set(VALID_TLDS)].sort((a, b) => b.length - a.length).join("|")})`;

const URL_REGEX = new RegExp(
  `^(?:https?|mailto|tel|sms|callto|facetime|skype|whatsapp|viber|tg|slack|discord|javascript|data|blob|file|ftp|irc|telnet|mms|rtsp|svn|git|ssh|sftp|wss?):(?:\\/\\/)?(?:\\S+@)?(?:localhost|(?:\\d{1,3}\\.){3}\\d{1,3}|\\[[a-f0-9:]+\\]|(?:(?:[a-z0-9\\u00A1-\\uFFFF][\\w\\u00A1-\\uFFFF-]{0,62})?[a-z0-9\\u00A1-\\uFFFF]\\.)+${VALID_TLD_PATTERN}\\.?)(?::\\d{2,5})?(?:[\\/?#]\\S*)?$`,
  "i"
);

/**
 * Check if a string has a valid URL format.
 *
 * @param str - The string to check.
 * @returns `true` if the string is a valid URL, otherwise `false`.
 */
export function isValidURL(str: string): boolean {
  return URL_REGEX.test(str);
}
